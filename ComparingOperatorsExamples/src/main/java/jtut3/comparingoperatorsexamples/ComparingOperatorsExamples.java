package jtut3.comparingoperatorsexamples;

/**
 *
 * @author DELL
 */
public class ComparingOperatorsExamples {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        double a = 2.3, b = 3.5;
        
        System.out.println(a + " > " + b + " = " + (a > b));
        System.out.println(a + " < " + b + " = " + (a < b));
        System.out.println(a + " >= " + b + " = " + (a >= b));
        System.out.println(a + " <= " + b + " = " + (a <= b));
        System.out.println(a + " == " + b + " = " + (a == b));
        System.out.println(a + " != " + b + " = " + (a != b));
    }
    
}
